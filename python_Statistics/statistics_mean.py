# Types of Statistics : 1- Descriptive   2- Infrential
# 1- Descriptive : 1- Measure of central tendency (Mean, Mode, Median)
#                  2- Measure of variability (Range, Variance, Standard Deviation)


# Mean: Measure of average of all value in a sample set
# Mean = sum of total sample / number of sample

marks = [75,82,88,94,67,34,65]
mean_marks = sum(marks)/len(marks)
print(mean_marks)
print("Round mean marks", round(mean_marks))
